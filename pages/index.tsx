import Head from "next/head";
import { useEffect, useState } from "react";
import Header from "../components/header";
import Footer from "../components/footer";
import Resume from "../components/resume";

function initialThemeIsDark() {
  let initialThemeIsDark = true;

  if (typeof window !== "undefined") {
    initialThemeIsDark =
      localStorage.theme === "dark" ||
      window.matchMedia("(prefers-color-scheme: dark)").matches;
  }

  return initialThemeIsDark;
}

const Home = () => {
  const [isDarkMode, setIsDarkMode] = useState(initialThemeIsDark());

  useEffect(() => {
    localStorage.setItem("theme", isDarkMode ? "dark" : "light");
  }, [isDarkMode]);

  const headerProps = { isDarkMode, setIsDarkMode };

  return (
    <div className={isDarkMode && "dark"}>
      <Head>
        <title>Portfolio</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <div className="bg-white dark:bg-slate-900 text-slate-500 dark:text-slate-400 flex justify-center duration-500">
        <main className="p-10 max-w-screen-lg">
          <div>
            <Header {...headerProps} />

            <div className="my-10">
              <h1 className="text-6xl mb-5 text-black dark:text-white">
                Hi, I am Simon
              </h1>

              <p className="text-slate-500">
                Lorem ipsum Lorem ipsum dolor sit amet, consectetur adipiscing
                elit, sed do eiusmod tempor incididunt ut labore et dolore magna
                aliqua.
              </p>
            </div>

            <Resume />
          </div>
        </main>
      </div>

      <Footer />
    </div>
  );
};

export default Home;
